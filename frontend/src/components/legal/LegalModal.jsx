function LegalModal({ type, onClose }) {
  const content = CONTENT[type];

  useEffect(() => {
    const onKey = (e) => { if (e.key === 'Escape') onClose(); };
    document.addEventListener('keydown', onKey);
    document.body.style.overflow = 'hidden';
    return () => {
      document.removeEventListener('keydown', onKey);
      document.body.style.overflow = '';
    };
  }, [onClose]);

  if (!content) return null;

  return (
    <div className="legal-modal__overlay" onClick={onClose}>
      <div className="legal-modal__dialog" onClick={(e) => e.stopPropagation()}>
        <div className="legal-modal__header">
          <h1 className="legal-modal__title">{content.title}</h1>
          <button className="legal-modal__close" onClick={onClose} aria-label="Закрыть">✕</button>
        </div>
        <div className="legal-modal__body">
          {content.body}
        </div>
      </div>
    </div>
  );
}

export default LegalModal;
